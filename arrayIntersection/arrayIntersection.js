function arrayIntersection(inputArray1, inputArray2) {
  let intersection = [];
  for (let i = 0; i < inputArray1.length; i++) {
    for (let j = 0; j < inputArray2.length; j++) {
      if (inputArray1[i] == inputArray2[j]) {
        intersection.push(inputArray1[i]);
        break;
      }
    }
  }
  return intersection;
}

