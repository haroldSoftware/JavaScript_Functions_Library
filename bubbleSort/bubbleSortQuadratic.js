function swap(inputArray, i, j) {
  temp = inputArray[i];
  inputArray[i] = inputArray[j];
  inputArray[j] = temp;
}

//==============================================================================
//=========================Bubble_Sort_Quadratic_Time===========================
//==============================================================================

function bubbleSort(inputArray) {

  lenArray = inputArray.length - 1;
  sorted = false;

  while (!sorted){
    sorted = true;
    for (let i = 0; i < lenArray; i++){
      if (inputArray[i] > inputArray[i+1]){
        sorted = false;
        swap(inputArray, i, i+1);
      }
    }
    lenArray--;
  }
  return inputArray;

}

